package yanevskyy;

/**
 * Uses as subitem menu.
 * @author Yanevskyy Igor igor2000@inbox.ru.
 */
public class SubItem {
    /*Item name*/
    private String name;

    /**
     * Default constructor.
     * @param name
     */
    public SubItem(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }
}
